'''
学习如何增删改查列表
    增加新元素
    删除元素
    修改元素
    查找元素 ：通过下标查找内容，或通过元素查找index mylist.index("H")
    查找元素出现次数：my_list.count(8)
'''
a = [1,2,3,4,5,6]
print(a.index(5)) #返回第一次出现的下标
print(a.count(5)) #元素5 出现一次

a = ["周一","周二","周三","周五","周四","周六","周日"]
a[3],a[4] = a[4],a[3] #元素交换位置
print(a)

s = "周一,周二,周三,周四,周五,周六,周日"
a = s.split(",")  #字符串分割后就是列表格式
print(a)
s2 = "".join(a) #将列表变成字符串
print(s2)
s3 = "*".join(a) #拼接时可以用“*”定义的符合连接
print(s3)

